import type { Metadata, Viewport } from "next";
import { connection } from "next/server";
import type { ReactNode } from "react";

import { AppHeader } from "@/components/layout/app-header";

import "./globals.css";

function publicAppUrl(): URL | undefined {
  const value = process.env.NEXT_PUBLIC_APP_URL;
  if (!value) return undefined;
  try {
    return new URL(value);
  } catch {
    return undefined;
  }
}

export const metadata: Metadata = {
  metadataBase: publicAppUrl(),
  title: {
    default: "Not Enough Bingo",
    template: "%s — Not Enough Bingo",
  },
  description: "Create, play, and share community bingo boards.",
};

export const viewport: Viewport = {
  width: "device-width",
  initialScale: 1,
  colorScheme: "light",
  themeColor: "#ffffff",
};

export default async function RootLayout({ children }: Readonly<{ children: ReactNode }>) {
  // Per-request rendering lets Next apply the nonce generated by proxy.ts to
  // every framework script and inline style element.
  await connection();
  return (
    <html lang="en" data-scroll-behavior="smooth">
      <body>
        <a className="skip-link" href="#main-content">
          Skip to content
        </a>
        <AppHeader />
        {children}
      </body>
    </html>
  );
}
